    private void HacerCalculos(){
        int marca,modelo;
        Double total = 0.00;
        String print;
        
        marca = lstMarca.getSelectedIndex();
        total+= CalcularMarca(marca);
        
        modelo = cbxModelo.getSelectedIndex();
        total+= CalcularMarcaModelo(marca,modelo);
        
        total+= CalcularCheckbox();
        total+= CalcularRadios();
        
        total = (total * CalcularDias()) * 1.13;
        
        print = String.valueOf(total);
        txtResultado.setText(print);    
    }
    
    private void CrearListaModelos(int marca){
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        
        switch (marca) {
            case 0:
                modelo.addElement("BMW 328");
                modelo.addElement("BMW 507");
                break;
            case 1:
                modelo.addElement("COROLLA");
                modelo.addElement("YARIS");
                break;
            case 2:
                modelo.addElement("SENTRA");
                modelo.addElement("VERSA");
                break;
            case 3:
                modelo.addElement("ACCENT");
                modelo.addElement("ELANTRA");
                break;
            default: 
                modelo.addElement("SELECCIONE MARCA");
                break;
        }
        this.cbxModelo.setModel(modelo);
    }
    
    private int CalcularMarca(int marca){
        int precio = 0;
        if(marca==0){
            precio = 50;
        }else if(marca==1){
            precio = 25;
        }else if(marca==2){
            precio = 20;
        }else if(marca==3){    
            precio = 18;
        }
        return precio;
    }
    
    private int CalcularMarcaModelo(int marca, int modelo){
        int precio = 0;
        if(marca==0){
            if(modelo==0){
                precio = 5;
            }else if(modelo==1){
                precio = 10;
            }
        }else if(marca==1){            
            if(modelo==0){
                precio = 20;
            }else if(modelo==1){
                precio = 25;
            }
        }else if(marca==2){            
            if(modelo==0){
                precio = 15;
            }else if(modelo==1){
                precio = 10;
            }
        }else if(marca==3){            
            if(modelo==0){
                precio = 10;
            }else if(modelo==1){
                precio = 15;
            }
        }
        return precio;
    }
    
    private int CalcularCheckbox(){
        int total = 0;
        if (this.chkSunrof.isSelected()) {
            total+= 10;
        }
        if (this.chkStereo.isSelected()) {
            total+= 25;
        }
        if (this.chkRines.isSelected()) {
            total+= 20;
        }        
        return total;
    }
    
    private int CalcularRadios(){
        int total = 0;
        if (this.rdoManual.isSelected()) {
            total+= 15;
        }
        if (this.rdoAutomatico.isSelected()) {
            total+= 20;
        }       
        return total;
    }
    
    private int CalcularDias(){
        int dias = sldDias.getValue();
        return dias;
    }

